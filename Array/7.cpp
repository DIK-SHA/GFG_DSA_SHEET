class Solution{
  public:
    int MissingNumber(vector<int>& array, int n) {
        // Your code goes here
        long long int sum=0;
        long long int orignal=0;
        orignal=(n*(n+1))/2;
        for(int i=0;i<n-1;i++)//isko n-1 tk chalay hai ye yaad rakho
        {
            sum+=array[i];
        }
        return orignal-sum;
    }
};

